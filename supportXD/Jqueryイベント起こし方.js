$(function() {
    //例
    
    //クリック時
    $(document).on('click', 'イベントを起こしたい所のclassとかid', function() {

    });

    //ダブルクリック時
    $(document).on('dbclick', 'イベントを起こしたい所のclassとかid', function() {

    });

    //フォームにある中の要素が変わった時
    $(document).on('change', 'イベントを起こしたい所のclassとかid', function() {

    });

    //マウスを押した時
    $(document).on('mousedown', 'イベントを起こしたい所のclassとかid', function() {

    });

    //マウスを押してから、離した時
    $(document).on('mouseup', 'イベントを起こしたい所のclassとかid', function() {

    });

    //右クリック
    $(document).on('contextmenu', 'イベントを起こしたい所のclassとかid', function() {

    });

    //マウスが要素の上で移動した時
    $(document).on('mousemove', 'イベントを起こしたい所のclassとかid', function() {

    });

    //マウスが要素の上にある時
    $(document).on('mouseover', 'イベントを起こしたい所のclassとかid', function() {

    });

    //マウスが要素の上から離れた時
    $(document).on('mouseout', 'イベントを起こしたい所のclassとかid', function() {

    });
});